package model;

public class Salesman {
	
	private String name;
	private Integer qtdProducts;
	private Integer qtdBrands;
	

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getQtdProducts() {
		return qtdProducts;
	}
	public void setQtdProducts(Integer qtdProducts) {
		this.qtdProducts = qtdProducts;
	}
	public Integer getQtdBrands() {
		return qtdBrands;
	}
	public void setQtdBrands(Integer qtdBrands) {
		this.qtdBrands = qtdBrands;
	}
	
}
